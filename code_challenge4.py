num1 = eval(input("Enter the first number: "))
num2 = eval(input("Enter the second number: "))

#Addition
sum = num1 + num2
print("\nThe sum of " , num1 , "and" , num2 , "is" , sum)

#Subtraction
difference = num1 - num2
print("The difference of " , num1 , "and" , num2 , "is" , difference)

#Multiplication
product = num1 * num2
print("The product of " , num1 , "and" , num2, "is" , product)

#Division
quotient = num1 / num2
print("The qoutient of " , num1 , "and" , num2, "is" , quotient)

#Exponent
exponent = num1 ** num2
print("The exponent of " , num1 , "and" , num2, "is" , exponent)

#Modulus
remainder = num1 % num2
print("The remainder of " , num1 , "and" , num2, "is" , remainder)

#Floor Division
FloorDivision = num1 // num2
print("The Floor Division of " , num1 , "and" , num2, "is" , FloorDivision)

